import { combineReducers } from "redux";


// Used to store movies returned from the server
const movies = (state = [], action) => {
    switch (action.type) {
        case 'SET_MOVIES':
            return action.payload;
        default:
            return state;
    }
}

// Used to store the movie genres
const genres = (state = [], action) => {
    switch (action.type) {
        case 'SET_GENRES':
            return action.payload;
        default:
            return state;
    }
}

// Used to store details on request from saga
const details = (state = [], action) => {
    switch (action.type) {
        case 'SET_DETAILS':
            return action.payload;
        default:
            return state;
    }
}

// lesson learned: put rootReducer BELOW referenced reducers
// because those don't hoist for some reason
const rootReducer = combineReducers({
    movies,
    genres,
    details
})

export default rootReducer;